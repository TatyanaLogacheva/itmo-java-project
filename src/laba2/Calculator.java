package laba2;

public class Calculator {

    public void sum( int x, int y) {
        System.out.println(x+y);
    }
    public void sum(double x, double y){
        System.out.println(x+y);
    }
    public void sum(long x, long y){
        System.out.println(x+y);
    }
    public void subtraction (int x, int y){
        System.out.println(x-y);
    }
    public void subtraction (double x, double y){
        System.out.println(x-y);
    }
    public void subtraction (long x, long y){
        System.out.println(x-y);
    }
    public void multiple (int x, int y){
        System.out.println(x*y);
    }
    public void multiple (double x, double y){
        System.out.println(x*y);
    }
    public void multiple (long x, long y){
        System.out.println(x*y);
    }
    public void division (int x, int y){
        System.out.println(x*1.0/y);
    }
    public void division (double x, double y){
        System.out.println(x/y);
    }
    public void division (long x, long y){
        System.out.println(x*1.0/y);
    }
}
